    private Icon buscarImagen()
    {
        FileDialog dialog = new FileDialog(this, "Buscar Imagen", FileDialog.LOAD);
        dialog.setVisible(true);
        ImageIcon img = new ImageIcon(dialog.getDirectory() + dialog.getFile());
        Icon icon = new ImageIcon(img.getImage().getScaledInstance(this.lbl_imagen_ico.getWidth(), this.lbl_imagen_ico.getHeight(), Image.SCALE_DEFAULT));
        ruta = dialog.getDirectory() + dialog.getFile();
        return icon;
    }

    private ImageIcon convertirImagen(byte[] foto,int width ,int height )
    {
        ImageIcon mIcono = null;
        if (foto.length != 0) 
        {
            try 
            {
                byte[] imagen = foto;
                BufferedImage bufferedImage = null;
                InputStream inputStream = new ByteArrayInputStream(imagen);
                bufferedImage = ImageIO.read(inputStream);
                mIcono = new ImageIcon(bufferedImage.getScaledInstance(width, height, 0));
                
            }
            catch (Exception e)
            {
                JOptionPane.showMessageDialog(null, "Error de conexi√≥n:" + e.getMessage() + "CONSULTS");
            }
        } 

        return mIcono;
    }

